export function addTheme(theme: Theme) {
  const existingIndex = themes.findIndex((t) => t.name === theme.name);
  if (existingIndex !== -1) {
    themes[existingIndex] = theme;
  } else {
    themes.push(theme);
  }
  saveThemesToStorage();
}

export function saveThemesToStorage() {
  if (typeof window !== "undefined") {
    localStorage.setItem("kquires-themes", JSON.stringify(themes));
  }
}

